  lines.push("operation on the parsed structure — no LLM re-interpretation. Refiner's Phase 6");
  lines.push('mechanical authorization check iterates over every `<trace>` element citing a Q&A');
  lines.push("row by `q_id` and verifies the cited content appears in that row's");
  lines.push('`user_answer_verbatim`.');
