  lines.push('');
  lines.push('```yaml');
  lines.push('- q_id: q1');
  lines.push('  question: "Which auth provider should the v2 spec target?"');
  lines.push('  user_answer_verbatim: "Auth0 — we already have an org account."');
  lines.push('  deferral_signal: false');
