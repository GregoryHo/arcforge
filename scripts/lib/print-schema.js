function renderPendingConflict(opts = {}) {
  const md = !!opts.markdown;
  const r = PENDING_CONFLICT_RULES;
  const lines = [];
  if (md) lines.push(...GENERATED_HEADER);
  lines.push('# _pending-conflict.md Schema');
  lines.push('');
  lines.push(`Canonical path: ${fmtCode(r.canonical_path, md)}`);
  lines.push('');
  lines.push(
    'The underscore prefix marks the file as ephemeral hand-off, not versioned spec content.',
  );
  lines.push(
    'The file MUST NOT live under `details/` or any path that would be parsed as a spec detail file.',
  );
  lines.push('');
  lines.push('## Lifecycle');
  lines.push('');
  lines.push(`**State**: ${r.lifecycle.state} — non-versioned, non-authoritative.`);
  lines.push('');
  lines.push(`- **Written by**: ${r.lifecycle.written_by}.`);
  lines.push(`- **Read by**: ${r.lifecycle.read_by}.`);
  lines.push(`- **Deleted by**: ${r.lifecycle.deleted_by}.`);
  lines.push('');
  lines.push('Per A.1\'s narrowed Iron Law, this is the explicit ephemeral exception to "no');
  lines.push('authoritative state on block". Validators MUST treat persistence across a completed');
  lines.push(
    `conflict cycle as ${r.lifecycle.persist_across_completed_cycle}: \`"a prior conflict cycle did not complete cleanly"\`.`,
  );
  lines.push('');
  lines.push('## Required Fields');
  lines.push('');
  lines.push('All four fields MUST be present. Missing any one field is an ERROR.');
  lines.push('');
  if (md) {
    lines.push('| Field | Type | Description |');
    lines.push('|---|---|---|');
    for (const f of r.required_fields) {
      let typeStr = f.type;
      if (Array.isArray(f.allowed)) {
        typeStr = `enum (${f.allowed.map((v) => `\`"${v}"\``).join(', ')})`;
      } else if (typeof f.min_length === 'number' || typeof f.max_length === 'number') {
        typeStr = `${f.type}, length ${f.min_length}–${f.max_length}`;
      }
      lines.push(`| \`${f.key}\` | ${typeStr} | ${f.description} |`);
    }
  } else {
    for (const f of r.required_fields) {
      let typeStr = f.type;
      if (Array.isArray(f.allowed)) {
        typeStr = `${f.type} (${f.allowed.join('|')})`;
      } else if (typeof f.min_length === 'number' || typeof f.max_length === 'number') {
        typeStr = `${f.type}[${f.min_length}..${f.max_length}]`;
      }
      lines.push(`  ${f.key.padEnd(24)} ${typeStr.padEnd(28)}  ${f.description}`);
    }
  }
  lines.push('');
  lines.push('## Examples');
  lines.push('');
  lines.push('### Valid example');
  lines.push('');
  lines.push(
    'Axis 1 fired, two candidate resolutions are present, and the user-action prompt tells the user exactly what to choose.',
  );
  lines.push('');
  lines.push('```yaml');
  lines.push('axis_fired: "1"');
  lines.push(
    'conflict_description: "REQ-A says export is CSV-only, but the new design asks for JSON as the default."',
  );
  lines.push('candidate_resolutions:');
  lines.push('  - "Keep CSV-only and reject JSON default."');
  lines.push('  - "Change REQ-A to allow JSON default and record the compatibility impact."');
  lines.push(
    'user_action_prompt: "Choose one candidate resolution, or provide a new explicit requirement."',
  );
  lines.push('```');
  lines.push('');
  lines.push('### Invalid example');
  lines.push('');
  lines.push(
    'No candidate resolutions are present. Error: "_pending-conflict.md MUST contain at least one candidate resolution".',
  );
  lines.push('');
  lines.push('```yaml');
  lines.push('axis_fired: "1"');
  lines.push(
    'conflict_description: "Existing spec and new design disagree about the export format."',
  );
  lines.push('candidate_resolutions: []');
  lines.push('user_action_prompt: "Please resolve the conflict."');
  lines.push('```');
  lines.push('');
  lines.push('## Enforcement Authority');
  lines.push('');
  lines.push(
    '`scripts/lib/sdd-rules.js` — `PENDING_CONFLICT_RULES` is the canonical source of truth',
  );
  lines.push('(re-exported via `scripts/lib/sdd-utils.js` for facade access). Validators built in');
  lines.push('`fr-sd-014` read from that constant. If this document disagrees with the constant,');
  lines.push('the constant wins and this document is wrong (file a bug).');
  return lines.join('\n');
}

// Back-compat aliases (terminal-mode renderers) still exported under their old names
// so existing tests (schema SoT + drift-detection) keep working.
function renderDesignHuman() {
  return renderDesign({ markdown: false });
}
function renderSpecHuman() {
  return renderSpec({ markdown: false });
}
function renderDesignMarkdown() {
  return renderDesign({ markdown: true });
}
function renderSpecMarkdown() {
  return renderSpec({ markdown: true });
}

// Custom JSON serializer that replaces RegExp with a string form so the output
// round-trips through tools that expect plain JSON.
function jsonifyRules(rules) {
  return JSON.parse(
    JSON.stringify(rules, (_, v) => (v instanceof RegExp ? `<regex>${v.source}</regex>` : v)),
  );
}

const HELP = `print-schema.js — print the current SDD schema for an artifact target.

Usage:
  node scripts/lib/print-schema.js <target> [--markdown | --json]

Targets:
  design            Design doc schema (docs/plans/<spec-id>/<date>/design.md)
  spec              Spec identity header schema (specs/<spec-id>/spec.xml <overview>)
  decision-log      Structured decision-log schema (brainstorming Phase 2 output)
  pending-conflict  _pending-conflict.md handoff schema (refiner R3 block output)

Flags:
  --markdown  Emit the committed-markdown view. Wraps regex patterns in
              inline code fences and includes a "do not edit" header.
              Used to regenerate scripts/lib/sdd-schemas/*.md.
  --json      Emit JSON instead of text. RegExp values are encoded as
              <regex>PATTERN</regex> strings so the output is portable.
  --help      Show this help.

Rules source:
  scripts/lib/sdd-utils.js — DESIGN_DOC_RULES, SPEC_HEADER_RULES,
  DECISION_LOG_RULES, PENDING_CONFLICT_RULES.
  This script NEVER duplicates structured schema fields. Valid/invalid
  examples for decision-log and pending-conflict live in renderer prose
  per fr-sd-013-ac3/fr-sd-012-ac3.`;

function main(argv) {
  const args = argv.slice(2);
  if (args.length === 0 || args.includes('--help') || args.includes('-h')) {
    process.stdout.write(`${HELP}\n`);
    return 0;
  }
  const target = args[0];
  const asJson = args.includes('--json');
  const asMarkdown = args.includes('--markdown');
  if (asJson && asMarkdown) {
    process.stderr.write('Error: --json and --markdown are mutually exclusive.\n');
    return 1;
  }
  const RULES_BY_TARGET = {
    design: DESIGN_DOC_RULES,
    spec: SPEC_HEADER_RULES,
    'decision-log': DECISION_LOG_RULES,
    'pending-conflict': PENDING_CONFLICT_RULES,
  };
  const RENDERER_BY_TARGET = {
    design: renderDesign,
    spec: renderSpec,
    'decision-log': renderDecisionLog,
    'pending-conflict': renderPendingConflict,
  };
  const RULES = RULES_BY_TARGET[target] ?? null;
  const renderer = RENDERER_BY_TARGET[target] ?? null;
  if (!RULES || !renderer) {
    process.stderr.write(`Unknown target: ${target}\n\n${HELP}\n`);
    return 1;
  }
  let out;
  if (asJson) out = JSON.stringify(jsonifyRules(RULES), null, 2);
  else out = renderer({ markdown: asMarkdown });
  process.stdout.write(`${out}\n`);
  return 0;
}

if (require.main === module) {
  process.exit(main(process.argv));
}

